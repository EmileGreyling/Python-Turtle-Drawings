# How To Impress your crush with Python


from turtle import *

# Set background color to black
bgcolor("black")

# Set turtle color to read
color("red")

begin_fill()
pensize(3)

# Start Drawing
left(50)
forward(133)
circle(50, 200)
right(140)
circle(50, 200)
forward(133)
end_fill()

# Hide the turtle
hideturtle()

done()
